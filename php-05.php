課題2
課題1で調べた結果、なんのことを言っているのかわからない場合はそれについて調べてみてください。
1.コードが読みやすく、書きやすい
　⇒OK
2.学習コストの低さ
　⇒難易度が低く習得しやすい
3.強力なルーティングフィルター
　⇒ルーティングフィルター：ルートフィルターとは、ルートへのアクセス制限（フィルター）を行うための定義ファイルです
4.柔軟なオートローダー
　⇒オートローダー：ファイルを自動で読み込む仕組みのこと。
5.簡単なページネーション
　⇒ページネーション：ウェブサイトのページ構成の一つで、長い文章などを複数のページに分割して表示するもの。
6.ビューをサポートするコンポーサー
　⇒ビュー：HTML出力の役割を受け持ちます
　　コンポーサー：PHPのパッケージ管理システム
　
