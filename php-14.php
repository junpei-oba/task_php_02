課題1
データベースとテーブルの関係を説明してください。
→データベースにはさまざまな種類のデータが格納されており、
このデータベースの種類ごとの単位を「テーブル」と呼ぶ。
データベースのテーブルはよくExcelで例えられることが多く、Excelのシートがテーブルを表す。
データベースは複数のテーブルを保持し、テーブルごとにデータを管理します。

